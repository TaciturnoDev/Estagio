package Sequencia.java;
public class Sequencias {
    public static void main(String[] args) {
        System.out.println("Próximos elementos nas sequências:");
        
        // Sequência (a): 1, 3, 5, 7, ___
        int sequenciaA = 7 + 2;
        System.out.println("a) " + sequenciaA);

        // Sequência (b): 2, 4, 8, 16, 32, 64, ____
        int sequenciaB = 64 * 2;
        System.out.println("b) " + sequenciaB);

        // Sequência (c): 0, 1, 4, 9, 16, 25, 36, ____
        int sequenciaC = 7 * 7;
        System.out.println("c) " + sequenciaC);

        // Sequência (d): 4, 16, 36, 64, ____
        int sequenciaD = 10 * 10;
        System.out.println("d) " + sequenciaD);

        // Sequência (e): 1, 1, 2, 3, 5, 8, ____
        int sequenciaE = 5 + 8;
        System.out.println("e) " + sequenciaE);

        // Sequência (f): 2, 10, 12, 16, 17, 18, 19, ____
        int sequenciaF = 20;
        System.out.println("f) " + sequenciaF);
    }
}
